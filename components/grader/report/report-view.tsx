import { FixPlanSection } from "./fix-plan";
import { GuestExperienceSection } from "./guest-experience";
import { LocalListingsSection } from "./local-listings";
import { ScoreHero } from "./score-hero";
import { SearchVisibilitySection } from "./search-visibility";
import { ReviewedItemsSummary, WebsiteSeoChecklist } from "./seo-checklist";
import { TopSummaryGrid } from "./summary-cards";
import { UnlockModal } from "./unlock-modal";

/** Deterministic (UTC, fixed locale) so server and client render identically. */
function formatAuditDate(iso: string): string {
  return new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
    timeZone: "UTC",
  }).format(new Date(iso));
}


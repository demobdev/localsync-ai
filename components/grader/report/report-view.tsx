export function GraderReport({
  report,
  signedIn,
}: {
  report: AuditReport;
  signedIn: boolean;
}) {
  const locked = !report.leadCaptured;
  const problems = deriveTopProblems({
    checks: report.checks,
    keywords: report.keywords,
    hasWebsite: report.websiteUrl !== null,
  });
  const fixPlan = deriveFixPlan(report.checks);

  return (
    <div className="min-h-full bg-[#faf7ef]">
      <ReportHeader signedIn={signedIn} />

      <main
        className={cn(
          "mx-auto max-w-5xl space-y-5 px-4 py-6 sm:px-6 sm:py-8",
          locked && "pointer-events-none blur-md select-none",
        )}
        aria-hidden={locked}
      >
        <AuditScopeBanner
          operatingModel={report.operatingModel}
          auditTier={report.auditTier}
          gbpLinked={report.gbpProfile.gbpLinked !== false}
          className="mb-1"
        />
        <ScoreHero report={report} />
        <TopSummaryGrid report={report} problems={problems} />
        <SearchVisibilitySection report={report} />
        <ReviewedItemsSummary report={report} />
        <WebsiteSeoChecklist report={report} />
        <GuestExperienceSection report={report} />
        <LocalListingsSection report={report} />
        <FixPlanSection fixPlan={fixPlan} />
        <ImproveWithAICTA report={report} signedIn={signedIn} />

        <footer className="pt-4 pb-16 text-center text-xs text-zinc-400">
          <p>
            Audit generated by LocalSync Grader ·{" "}
            {formatAuditDate(report.createdAt)} · scores reflect publicly
            visible signals at scan time.
          </p>
        </footer>
      </main>

      {locked ? (
        <UnlockModal report={report} />
      ) : (
        <StickyFixCTA report={report} signedIn={signedIn} />
      )}
    </div>
  );
}
